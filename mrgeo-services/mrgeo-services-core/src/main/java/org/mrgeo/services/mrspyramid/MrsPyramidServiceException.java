package org.mrgeo.services.mrspyramid;

public class MrsPyramidServiceException extends java.lang.Exception
{

/**
 *
 */
private static final long serialVersionUID = 1L;

public MrsPyramidServiceException()
{
}

public MrsPyramidServiceException(String msg)
{
  super(msg);
}

public MrsPyramidServiceException(String msg, Throwable throwable)
{
  super(msg, throwable);
}

public MrsPyramidServiceException(Throwable throwable)
{
  super(throwable);
}
}
